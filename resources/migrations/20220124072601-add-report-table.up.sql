CREATE TABLE report
(id INTEGER PRIMARY KEY AUTOINCREMENT,
section_1_title VARCHAR(500),
section_1_text VARCHAR(5000),
section_2_title VARCHAR(500),
section_2_text VARCHAR(5000),
section_3_title VARCHAR(500),
section_3_text VARCHAR(5000),
section_4_title VARCHAR(500),
section_4_text VARCHAR(5000),
timestamp TIMESTAMP(7));
